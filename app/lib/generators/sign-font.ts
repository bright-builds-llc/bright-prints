export type SupportedGlyph = keyof typeof bitmapFont;

const supportedGlyphSet = new Set<SupportedGlyph>(
  Object.keys(bitmapFont) as SupportedGlyph[]
);

export function sanitizeSignText(text: string) {
  const normalized = text.toUpperCase().trim().replace(/\s+/g, " ");
  let unsupported = false;

  const sanitized = [...normalized]
    .map((character) => {
      if (supportedGlyphSet.has(character as SupportedGlyph)) {
        return character as SupportedGlyph;
      }

      unsupported = true;
      return " " as SupportedGlyph;
    })
    .join("");

  return {
    hadUnsupportedGlyphs: unsupported,
    sanitizedText: sanitized
  };
}
